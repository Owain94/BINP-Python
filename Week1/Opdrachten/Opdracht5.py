def calculate_distance(speed, time):
    return int(speed) * int(time)


print("{} {}".format(str(calculate_distance(70, 6)), "miles"))
print("{} {}".format(str(calculate_distance(70, 10)), "miles"))
print("{} {}".format(str(calculate_distance(70, 15)), "miles"))

